package Operators.ComparisonRelationalOperators;

public class NotEqualSignOperator {

    public static void main(String[] args) {

        int variableOne = 100;
        int variableTwo = 50;

        if (variableOne != variableTwo) {
            System.out.println("True");
        }
        else {
            System.out.println("False");
        }
    }
    
}
