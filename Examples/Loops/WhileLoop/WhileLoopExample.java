public class WhileLoopExample {

    public static void main(String[] args) {

        int number = 10;

        while (number >= 5) 
        {
            System.out.println("The values after decreament are: " + number);
            number--;
        }
        while (number <= 10)
        {
            System.out.println("The values after decreament are: " + number);
            number++;
        }
    }
    
}
