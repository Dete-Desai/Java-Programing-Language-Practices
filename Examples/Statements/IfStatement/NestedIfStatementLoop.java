
public class NestedIfStatementLoop {

    public static void main(String[] args) {
        
        int number = 100;

        if (number < 200) {

            System.out.println("The number is less than 200");

            if (number == 100) {

                System.out.println("The number is equal to 100");
            }
        }
    }
    
}
